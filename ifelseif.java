public class ifelseif {

    public static void main(String[] args) {
        int marks = 92;
        if(marks>=90){
            System.out.println("A+");
        }
        else if(marks>=75){
            System.out.println("A");
        }
        else{
            System.out.println("fail");
        }
    }
}

